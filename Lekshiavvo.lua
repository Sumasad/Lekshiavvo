-- list running observer
function observeRunning(list)
	if not title then
		title = dataset[list + 1].title
		runListSequence(dataset[list + 1].rows)
		running = true
		pause = false
		sampAddChatMessage("{DC143C}[BBO-Sila]  {FFFFFF}Лекция: {FFFF00}" .. title .. " {008000}начата.", 0xFAFAD2)
	end
end

-- list rows sequence
function runListSequence(list)
	lua_thread.create(function()
		for i, row in ipairs(list) do
			local observe = row.delay / step
			for i = 1, observe do
				while pause do wait(0) end
				wait(tonumber(step))
			end
			sampSendChat(replace(row.message)) 
		end
		wait(step)
		clearStates(true)
	end)
end
		

-- stateful clear
function clearStates(notify)
	if notify then
		sampAddChatMessage(" {FFFFFF}Лекция: {FFFF00}" .. title .. " {8B0000}окончена.", 0xFAFAD2)
	end
	pause = false
	running = nil
	title = nil
end

