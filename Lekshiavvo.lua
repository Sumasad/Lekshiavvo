function replace(msg)
	local _, id = sampGetPlayerIdByCharHandle(PLAYER_PED)
	local nick = sampGetPlayerNickname(id)
	nick = nick:match("%a+%_(%a+)")
	msg = msg:gsub('{nickname}' , nick)
	msg = msg:gsub('{rang}', MYmembers)
		return msg
end

